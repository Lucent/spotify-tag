  <h2>Use Playlists as Tags on Spotify</h2>

  <p>Have you tried to categorize your music using playlists only to realize you can't see which songs haven't yet been put into a playlist? Organizing your music can be daunting if there's no way of knowing when you're done.</p>
  <p>With Tagify, your existing playlists will stay untouched, but a new playlist is created containing any music in your library which isn't also on a playlist with the prefix <code>tag:</code></p>
  <p>Simply populate these new <code>tag:indie</code> or <code>tag:electronic</code> playlists you create with songs, and watch them disappear from a new <code>tags:Untagged</code> playlist this tool creates which is simply your saved library minus your tagged songs.</p>

